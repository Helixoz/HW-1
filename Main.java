package com.company;

public class Main {

    public static void main(String[] args) {
       byte a = 8;
       short b = 10;
       int c = 15;
       long d = 20l;
       double s = 13d;

        System.out.println("Result + : " + (a + b));
        System.out.println("Result - : " + (c - d));
        System.out.println("Result * : " + (b * s));
        System.out.println("Result / : " + (s / d));
    }
}
